package com.nttdata.knot.iacapi.Models.UserPackage;

public class ProjectRoles {
    private String label;
    private String id;
    private String github_role;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGithub_role() {
        return github_role;
    }

    public void setGithub_role(String github_role) {
        this.github_role = github_role;
    }
}

